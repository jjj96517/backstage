package com.piaoniu.backstage.pojo;

public class Hall {
    private int hall_id;
    private String hall_name;
    private int show_place_id;
    private int hall_seatnum;

    public int getHall_id() {
        return hall_id;
    }

    public void setHall_id(int hall_id) {
        this.hall_id = hall_id;
    }

    public String getHall_name() {
        return hall_name;
    }

    public void setHall_name(String hall_name) {
        this.hall_name = hall_name;
    }

    public int getShow_place_id() {
        return show_place_id;
    }

    public void setShow_place_id(int show_place_id) {
        this.show_place_id = show_place_id;
    }

    public int getHall_seatnum() {
        return hall_seatnum;
    }

    public void setHall_seatnum(int hall_seatnum) {
        this.hall_seatnum = hall_seatnum;
    }
}
